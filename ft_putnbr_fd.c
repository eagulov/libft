#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char c;

	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		c = nb % 10 + '0';
		write(1, &c, 1);
	}
	else
	{
		c = nb + '0';
		write(fd, &c, 1);
	}
}
