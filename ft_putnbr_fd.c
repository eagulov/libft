#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char			c;
	unsigned int	num;

	if (nb < 0)
	{
		write(fd, "-", 1);
		num = (-1) * nb;
	}
	else
		num = nb;
	if (num > 9)
		ft_putnbr_fd(num / 10, fd);
	c = num % 10 + '0';
	write(fd, &c, 1);
}
